string generateCodiconToIdJson(List<List<string>> codiconToId)
{
    List<string> codiconsAdded = [];
    string result = "{";
    foreach (var item in codiconToId)
    {
        string codicon = item[0];
        if (codiconsAdded.Contains(codicon))
            continue;
        result += $"\"{codicon}\":[";
        codiconToId.FindAll(i => i[0] == codicon).ForEach(i => result += $"\"{i[1]}\",");
        result = result.TrimEnd(',') + "],";
        codiconsAdded.Add(codicon);
    }
    result = result.TrimEnd(',') + "}";
    return result;
}

File.WriteAllText("codicon-to-id.json", generateCodiconToIdJson(codiconToId));
